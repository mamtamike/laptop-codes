package learning;

public class RelationalOperators {
	public static void main(String[] args) {
		int number1 = 10;
		int myNumber = 20;
		
		if (number1==myNumber)
	    { 
			System.out.println("numbers are equal");
		}
		else
		{
			System.out.println("numbers are not equal");	
		}
		
		if (number1!=myNumber)
		{
			System.out.println("numbers are not equal");
		}
		else
		{
			System.out.println("numbers are equal");
		}
}
}


